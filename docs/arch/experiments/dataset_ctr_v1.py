def build_prompt(item: dict) -> str:
    """将单条数据构造成模型输入的 prompt"""
    return f"""文本1：{item["text1"]}

文本2：{item["text2"]}

问题：{item["question"]}

选项：
{item["options"]}

请分析以上两段文本，回答这个问题。直接说出你的答案（A、B 或 C），然后简要解释推理过程。"""


def get_dataset() -> list[dict]:
    return CONTRADICTION_DATASET


